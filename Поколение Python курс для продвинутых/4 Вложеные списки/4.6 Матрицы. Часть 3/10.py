n, m = 4,5  #map(int, input().split())
data = [[0 for j in range(m)] for i in range(n)]

trend = 'r'  # r - right, l - left, u - up, d - down
counter = 0
i = 0
j = 0


while counter != n * m:
    counter += 1

    if trend == 'r':

        if data[i][j] != 0:
            trend = 'd'
            j -= 1
            i += 1
            counter -= 1
        elif j == m-1:
            trend = 'd'
            data[i][j] = counter
            i += 1
        elif j >= 0 and j < m and data[i][j] == 0:
            data[i][j] = counter
            j += 1
        continue

    if trend == 'l':

        if data[i][j] != 0:
            trend = 'u'
            j += 1
            i -= 1
            counter -= 1
        elif j == 0:
            trend = 'u'
            data[i][j] = counter
            i -= 1
        elif j >= 0 and j < m and data[i][j] == 0:
            data[i][j] = counter
            j -= 1
        continue

    if trend == 'u':

        if data[i][j] != 0:
            trend = 'r'
            i += 1
            j += 1
            counter -= 1
        elif i == 0:
            trend = 'r'
            data[i][j] = counter
            j += 1
        elif i >= 0 and i < n and data[i][j] == 0:
            data[i][j] = counter
            i -= 1
        continue

    if trend == 'd':

        if data[i][j] != 0:
            trend = 'l'
            i -= 1
            j -= 1
            counter -= 1
        elif i == n - 1:
            trend = 'l'
            data[i][j] = counter
            j -= 1
        elif i >= 0 and i < n and data[i][j] == 0:
            data[i][j] = counter
            i += 1
        continue

