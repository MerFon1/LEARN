n = [int(i) for i in  input().split()]
newn = []
pp = 'empty'
c1 = 0
c2 = 1
while True:
    if c2 != len(n)+1:
        if len(n)%2==0:
            a = n[c1]
            b = n[c2]
            newn.append(b)
            newn.append(a)
            c1 += 2
            c2 += 2
        else:
            pp = n[len(n)-1]
            del n[len(n)-1]
    else:
        if pp != 'empty':
            newn.append(pp)
            break
        else:
            break
print(*newn)