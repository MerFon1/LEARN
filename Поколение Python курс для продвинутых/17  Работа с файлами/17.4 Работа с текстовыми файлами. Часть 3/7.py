def minutes(x):
    res=[int(i) for i in x.split(':')]
    return res[0]*60+res[1]

with open('logfile.txt',encoding='utf-8') as logs, open('output.txt','w',encoding='utf-8') as re:
    for line in logs:
        name,inp,out = line.strip().split(', ')
        if minutes(inp)<minutes(out) and minutes(out)-minutes(inp) > 59:
            print(name,file=re)
        elif minutes(inp)>minutes(out) and 1440 + minutes(out)-minutes(inp) >59:
            print(name,file=re)




